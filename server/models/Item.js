//MONGOOSE IMPORT
const mongoose = require('mongoose');
const { Schema } = mongoose;

//ITEM SCHEMA
const item = new Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String
  },
  image: {
    type: String
  },
  price: {
    type: Number,
    required: true,
    min: 1.00
  },
  quantity: {
    type: Number,
    min: 0,
    default: 0
  },
  category: {
    type: Schema.Types.ObjectId,
    ref: 'Category',
    required: true
  }
});

//ITEM MODEL
const Item = mongoose.model('Item', item);

module.exports = Item;
